let sigla = "RJ".toUpperCase();

switch (sigla) {
    
    case `RJ`:
        console.log(`Carioca`)
        break;

    case "SP":
        console.log("Paulista")
        break;

    case `MG`:
        console.log(`Mineiro`)
        break;

    default:
        console.log (`Acesso Negado`)
        break;
}